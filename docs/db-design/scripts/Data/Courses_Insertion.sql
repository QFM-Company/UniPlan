   3. إدخال البيانات في جدول [dbo].[Courses] بناءً على الهيكلية الأصلية
========================================================= */
INSERT INTO [dbo].[Courses] ([CourseCode], [CourseName], [CreditHours])
SELECT 
    sc.[CourseCode], 
    sc.[CourseName], 
    sc.[Credits]
FROM #SourceCourses sc
WHERE NOT EXISTS (
    SELECT 1 
    FROM [dbo].[Courses] c
    WHERE c.[CourseCode] = sc.[CourseCode]
);

