INSERT INTO [dbo].[Lectures] ([LectureType], [DurationValue], [CourseID])
SELECT s.[LectureType], s.[DurationMinutes], c.[CourseID]
FROM #SourceLectures s
INNER JOIN [dbo].[Courses] c ON c.[CourseName] = s.[CourseName]
WHERE NOT EXISTS (
    SELECT 1
    FROM [dbo].[Lectures] l
    WHERE l.[CourseID] = c.[CourseID]
      AND l.[LectureType] = s.[LectureType]
      AND l.[DurationValue] = s.[DurationMinutes]
);

DROP TABLE #SourceLectures;
GO

SELECT l.[LectureID], l.[LectureType], l.[DurationValue] AS [DurationMinutes], c.[CourseName], c.[CourseCode]
FROM [dbo].[Lectures] l
INNER JOIN [dbo].[Courses] c ON l.[CourseID] = c.[CourseID]
ORDER BY c.[CourseID], l.[LectureType];
